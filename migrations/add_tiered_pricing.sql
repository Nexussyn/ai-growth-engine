BEGIN;

CREATE TABLE IF NOT EXISTS public.pricing_tiers (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tier_code text NOT NULL CHECK (tier_code IN ('free', 'standard', 'premium', 'priority')),
  min_calls integer NOT NULL,
  max_calls integer,
  price_per_call_usd numeric(10,4) NOT NULL,
  notes text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT pricing_tiers_range_chk CHECK (max_calls IS NULL OR max_calls > min_calls)
);

CREATE UNIQUE INDEX IF NOT EXISTS pricing_tiers_tier_code_uq
  ON public.pricing_tiers (tier_code);

CREATE INDEX IF NOT EXISTS pricing_tiers_range_idx
  ON public.pricing_tiers (tier_code);

CREATE TABLE IF NOT EXISTS public.x402_api_calls (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id text NOT NULL,
  call_count integer NOT NULL DEFAULT 0,
  priority_flag boolean NOT NULL DEFAULT false,
  tier text NOT NULL DEFAULT 'free',
  price_per_call_usd numeric(10,4) NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT x402_api_calls_tier_chk CHECK (tier IN ('free', 'standard', 'premium', 'priority'))
);

ALTER TABLE IF EXISTS public.x402_api_calls
  ADD COLUMN IF NOT EXISTS tier text NOT NULL DEFAULT 'free';

ALTER TABLE IF EXISTS public.x402_api_calls
  ADD COLUMN IF NOT EXISTS price_per_call_usd numeric(10,4) NOT NULL DEFAULT 0;

CREATE INDEX IF NOT EXISTS x402_api_calls_user_tier_idx
  ON public.x402_api_calls (user_id, tier);

