CREATE OR REPLACE FUNCTION public.get_tier_price(call_count integer, priority_flag boolean)
RETURNS numeric(10,4)
LANGUAGE plpgsql
STABLE
AS $$
DECLARE
  resolved_price numeric(10,4);
BEGIN
  IF call_count IS NULL OR call_count < 0 THEN
    RAISE EXCEPTION 'call_count must be a non-negative integer';
  END IF;

  IF priority_flag THEN
    SELECT price_per_call_usd
      INTO resolved_price
      FROM public.pricing_tiers
     WHERE tier_code = 'priority'
     LIMIT 1;
  ELSIF call_count <= 50 THEN
    SELECT price_per_call_usd
      INTO resolved_price
      FROM public.pricing_tiers
     WHERE tier_code = 'free'
     LIMIT 1;
  ELSIF call_count <= 500 THEN
    SELECT price_per_call_usd
      INTO resolved_price
      FROM public.pricing_tiers
     WHERE tier_code = 'standard'
     LIMIT 1;
  ELSE
    SELECT price_per_call_usd
      INTO resolved_price
      FROM public.pricing_tiers
     WHERE tier_code = 'premium'
     LIMIT 1;
  END IF;

  RETURN resolved_price;
END;
$$;

CREATE OR REPLACE FUNCTION public.get_tier(call_count integer, priority_flag boolean)
RETURNS text
LANGUAGE plpgsql
STABLE
AS $$
BEGIN
  IF call_count IS NULL OR call_count < 0 THEN
    RAISE EXCEPTION 'call_count must be a non-negative integer';
  END IF;

  IF priority_flag THEN
    RETURN 'priority';
  ELSIF call_count <= 50 THEN
    RETURN 'free';
  ELSIF call_count <= 500 THEN
    RETURN 'standard';
  ELSE
    RETURN 'premium';
  END IF;
END;
$$;

CREATE OR REPLACE FUNCTION public.price_x402_call(call_count integer, priority_flag boolean)
RETURNS TABLE(tier text, price_per_call_usd numeric(10,4))
LANGUAGE plpgsql
STABLE
AS $$
BEGIN
  RETURN QUERY
  SELECT public.get_tier(call_count, priority_flag), public.get_tier_price(call_count, priority_flag);
END;
$$;

COMMIT;
